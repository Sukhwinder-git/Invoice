            $(document).ready(function () {
                $('#addTableBtn').click(function () {
                    var tableHtml = $('.tableWrapper:first').html();
                    $('#tableContainer').append('<div class="tableWrapper">' + tableHtml + '</div>');
                });

                $(document).on('click', '.add_description', function () {
                    $(this).siblings('.description').css("display", "block");
                });

                 // Auto-calculate values
                    $(document).on('input', '.editableTable .rate, .editableTable .quantity', function () {
                        updateTotals($(this).closest('.tableWrapper'));
                    });

                    // Initial calculation
                    $('.editableTable').each(function () {
                        updateTotals($(this).closest('.tableWrapper'));
                    });

                    function updateTotals($tableWrapper) {
                        var totalAmount = 0;
                        var totalIgst = 0;
                        var grandTotal = 0;

                        $tableWrapper.find('.editableTable tbody tr').each(function () {
                            var quantity = parseFloat($(this).find('.quantity').text()) || 0;
                            var rate = parseFloat($(this).find('.rate').text()) || 0;

                            var amount = quantity * rate;
                            var igst = amount * 0.18; // Assuming 18% IGST, adjust as needed
                            var total = amount + igst;

                            $(this).find('.amount').text(amount.toFixed(2));
                            $(this).find('.igst').text(igst.toFixed(2));
                            $(this).find('.total').text(total.toFixed(2));

                            totalAmount += amount;
                            totalIgst += igst;
                            grandTotal += total;
                        });

                        $tableWrapper.find('.totals .totalAmount').text(totalAmount.toFixed(2));
                        $tableWrapper.find('.totals .totalIgst').text(totalIgst.toFixed(2));
                        $tableWrapper.find('.totals .grandTotal').text(grandTotal.toFixed(2));
                    }
                });


                $(document).ready(function () {
                    $('#addFieldsBtn').click(function () {
                        // Customize and append additional input fields
